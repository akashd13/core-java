package com.xworkz.Java_Basics.Basics.Operators;

class Relationaloperators {
	
	public static void main(String args[]) {

		int x = 100;
		int y = 200;

		boolean equalResult = (x==y);
		boolean notequalResult = (x != y);
		boolean lessthanResult = x<y;
		boolean lesserthanequalResult = (x<=y);
		boolean greaterthanResult = x>y;
		boolean greaterthanequalResult = (x>=y);

		System.out.println("equalResult : " +equalResult);
		System.out.println("notequalResult : " +notequalResult);
		System.out.println("lessthanResult : " +lessthanResult);
		System.out.println("lesserthanequalResult : " +lesserthanequalResult);
		System.out.println("greaterthanResult : " +greaterthanResult);
		System.out.println("greaterthanequalResult : " +greaterthanequalResult);




	}
}